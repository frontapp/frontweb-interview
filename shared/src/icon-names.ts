export type IconName = (typeof ICON_NAMES)[number];

/** Format icon name for display (e.g. "add-column-left" → "Add Column Left") */
export function formatIconNameForDisplay(name: IconName): string {
  return name
    .split('-')
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
}
